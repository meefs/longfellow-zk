struct SimpleRng {
    state: u64,
}

impl RandomEngine for SimpleRng {
    fn bytes(&mut self, len: usize) -> Vec<u8> {
        let mut buf = Vec::with_capacity(len);
        for _ in 0..len {
            self.state = self
                .state
                .wrapping_mul(6_364_136_223_846_793_005)
                .wrapping_add(1_442_695_040_888_963_407);
            buf.push((self.state >> 32) as u8);
        }
        buf
    }
}

#[allow(clippy::too_many_arguments)]
fn make_raw_circuit<const W: usize, F: RuntimeField<W> + SerializableField>(
    f: &F,
    ninput: usize,
    npublic_input: usize,
    noutput: usize,
    logv: usize,
    layer_ninputs: &[usize],
    layer_logws: &[usize],
    layers_terms: Vec<Vec<Term<F>>>,
) -> RawCircuit<F> {
    let mut constants = Vec::new();
    let mut layers = Vec::new();
    let mut constant_to_idx = std::collections::HashMap::new();

    for (i, terms) in layers_terms.into_iter().enumerate() {
        let mut terms_sorted = terms;
        terms_sorted.sort_by(|a, b| compare_term(f, a, b));

        let mut deltas = Vec::new();
        let mut delta_to_idx = std::collections::HashMap::new();
        let mut delta_segment = Vec::with_capacity(terms_sorted.len());
        let mut prev_g = 0u32;
        let mut prev_h0 = 0u32;
        let mut prev_h1 = 0u32;

        for term in terms_sorted {
            let key = f.to_bytes(&term.k);
            let k_index = *constant_to_idx.entry(key).or_insert_with(|| {
                let idx = constants.len() as u32;
                constants.push(term.k.clone());
                idx
            });

            let dg = term.g.wrapping_sub(prev_g);
            let dh0 = term.h0.wrapping_sub(prev_h0);
            let dh1 = term.h1.wrapping_sub(prev_h1);

            let d = TermDelta {
                g: dg,
                h: [dh0, dh1],
                k_index,
            };

            let idx = *delta_to_idx.entry(d).or_insert_with(|| {
                let idx = deltas.len() as u32;
                deltas.push(d);
                idx
            });

            delta_segment.push(idx);

            prev_g = term.g;
            prev_h0 = term.h0;
            prev_h1 = term.h1;
        }

        let unrolled_deltas: Vec<core_proto::TermDelta> = delta_segment
            .iter()
            .map(|&idx| deltas[idx as usize])
            .collect();

        layers.push(Layer::new_compressed(
            layer_ninputs[i],
            layer_logws[i],
            vec![unrolled_deltas],
            vec![0],
        ));
    }

    RawCircuit {
        ninput,
        npublic_input,
        noutput,
        logv,
        subfield_boundary: 0,
        constants,
        layers,
    }
}

#[test]
fn test_zk_prover_verifier_end_to_end() {
    let f = Gf2_128Field::new();
    let subfield = BinarySubfield::new(&core_algebra::proto::GF2_16_BASIS_V1);

    // 1. Manually build a simple 1-layer circuit
    let term = Term {
        k: f.one(),
        g: 0,
        h0: 0,
        h1: 1,
    };
    let raw = make_raw_circuit(&f, 2, 0, 1, 0, &[2], &[1], vec![vec![term]]);
    let circ = Circuit { raw, id: [0u8; 32] };

    // 2. Setup inputs
    let mut w0 = [f.zero(); 2];
    w0[0] = f.one();
    w0[1] = f.zero();

    // 3. Prover commit
    let make_interpolator = Lch14InterpolatorFactory::new(&f, &subfield);

    let mut rng = SimpleRng { state: 42 };
    let mut ts_prover = Transcript::new(b"test_zk");

    let config = LigeroConfig {
        rateinv: 4,
        nreq: 16,
        block_enc: 256,
    };

    let sf = runtime_algebra::subfield::BinarySubfield::new(&core_algebra::proto::GF2_16_BASIS_V1);
    let prover_inst = ZkProver::new(circ, config);
    let (prover, geom) = prover_inst.commit(
        &w0[prover_inst.circuit.raw.npublic_input..],
        &runtime_zk::common::ZkContext {
            f: &f,
            make_interpolator: &make_interpolator,
        },
        &mut ts_prover,
        &mut rng,
        &sf,
    );

    // 4. Prover prove
    let zkp = prover_inst
        .prove(
            w0[..prover_inst.circuit.raw.npublic_input].to_vec(),
            w0[prover_inst.circuit.raw.npublic_input..].to_vec(),
            &prover,
            &mut ts_prover,
            &runtime_zk::common::ZkContext {
                f: &f,
                make_interpolator: &make_interpolator,
            },
        )
        .expect("Failed to prove");
    // 5. Serialize / deserialize proof roundtrip
    let serialized = zkp
        .write(&geom, &f, &sf)
        .expect("Failed to serialize ZkProof");
    let verifier = ZkVerifier::new(prover_inst.circuit, config);
    let v_geom = verifier.geometry(&runtime_zk::common::ZkContext {
        f: &f,
        make_interpolator: &make_interpolator,
    });
    let mut r_slice = serialized.as_slice();
    let deserialized =
        ZkProof::read(&mut r_slice, &v_geom, &f, &sf).expect("Failed to deserialize ZkProof");
    assert!(r_slice.is_empty());
    assert_eq!(
        zkp, deserialized,
        "Deserialized proof is not equal to original proof!"
    );

    // 6. Verifier verify
    let mut ts_verifier = Transcript::new(b"test_zk");
    verifier.recv_commitment(
        &deserialized,
        &mut ts_verifier,
        &runtime_zk::common::ZkContext {
            f: &f,
            make_interpolator: &make_interpolator,
        },
    );

    let verify_res = verifier.verify(
        w0[..verifier.circuit.raw.npublic_input].to_vec(),
        &deserialized,
        &mut ts_verifier,
        &runtime_zk::common::ZkContext {
            f: &f,
            make_interpolator: &make_interpolator,
        },
    );

    assert!(verify_res.is_ok(), "ZK Verification failed: {verify_res:?}");
}

