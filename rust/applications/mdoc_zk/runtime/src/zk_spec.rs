#[must_use]
pub fn find_zk_spec(system_name: &str, combined_hash: &[u8; 32]) -> Option<&'static ZkSpecStruct> {
    ZK_SPECS
        .iter()
        .find(|spec| spec.system == system_name && &spec.combined_hash == combined_hash)
}

#[must_use]
pub fn find_zk_spec_by_hex(
    system_name: &str,
    combined_hash_hex: &str,
) -> Option<&'static ZkSpecStruct> {
    ZK_SPECS
        .iter()
        .find(|spec| spec.system == system_name && spec.combined_hash_hex() == combined_hash_hex)
}

#[must_use]
pub fn is_valid_zk_spec_version(zk_spec: &ZkSpecStruct) -> bool {
    zk_spec.version >= 5 && zk_spec.version <= CURRENT_VERSION
}

#[must_use]
pub fn is_supported_version(version: usize) -> bool {
    (5..=CURRENT_VERSION).contains(&version)
}

#[must_use]
pub fn ligero_configs(zk_spec: &ZkSpecStruct) -> (LigeroConfig, LigeroConfig) {
    let rateinv = if zk_spec.version < 7 { 4 } else { 7 };
    let nreq = if zk_spec.version < 7 { 128 } else { 132 };
    let config_hash = LigeroConfig {
        rateinv,
        nreq,
        block_enc: zk_spec.block_enc_hash,
    };
    let config_sig = LigeroConfig {
        rateinv,
        nreq,
        block_enc: zk_spec.block_enc_sig,
    };
    (config_hash, config_sig)
}
