#[test]
fn test_reference_security_bounds_validation() {
    let parse_gf2_128 = |s: &str| {
        let val = BigInt::parse_bytes(s.as_bytes(), 10).unwrap();
        let mut v_bytes = val.to_bytes_le().1;
        v_bytes.resize(16, 0);
        Gf2_128::from_bytes(&v_bytes).unwrap()
    };

    let config = LigeroConfig {
        rate_inv: 3,
        num_queries: 16,
        encoded_len: 256,
    };

    // 1. Load Circuit
    let circuit_file = File::open(get_testdata_path("tests/testdata/sha256_circuit.lfc2"))
        .expect("Failed to open circuit file");
    let mut circuit_reader = BufReader::new(circuit_file);
    let circuit_data =
        parse_lfc2_bytes::<Gf2_128, _>(&mut circuit_reader).expect("Failed to parse LFC2 circuit");

    // 2. Load Inputs
    let inputs_file = File::open(get_testdata_path("tests/testdata/sha256_inputs.json"))
        .expect("Failed to open inputs file");
    let inputs_reader = BufReader::new(inputs_file);
    let raw_inputs: serde_json::Value =
        serde_json::from_reader(inputs_reader).expect("Failed to read JSON inputs");
    let inputs: Vec<Gf2_128> = raw_inputs
        .as_array()
        .unwrap()
        .iter()
        .map(|v| parse_gf2_128(v.as_str().unwrap()))
        .collect();

    // 3. Generate Valid Proof
    let mut rng = SimpleRng::new(12345);
    let prover = ZkProver::new(circuit_data.clone(), config.clone());
    let commit = prover.commit(&inputs, &mut rng);
    let valid_proof = prover.prove(&inputs, &commit, "security_test");

    let verifier = ZkVerifier::new(circuit_data, config);
    let npublic_input = prover.circuit_data.npublic_input;
    let public_inputs = &inputs[0..npublic_input];

    // Verify original proof is indeed valid
    assert!(
        verifier
            .verify(public_inputs, &valid_proof, "security_test")
            .is_ok()
    );

    // Mutation A: Corrupt sumcheck layers length (DoS prevention check)
    {
        let mut mutated_proof = valid_proof.clone();
        mutated_proof.sumcheck_proof.truncate(1); // truncate layer count
        let res = verifier.verify(public_inputs, &mutated_proof, "security_test");
        assert!(
            res.is_err(),
            "Verifier should have rejected truncated layers proof instead of panicking!"
        );
    }

    // Mutation C: Corrupt req length (Malleability check)
    {
        let mut mutated_proof = valid_proof.clone();
        mutated_proof
            .ligero_proof
            .queried_columns
            .push(Gf2_128::zero()); // append extra element to query elements
        let res = verifier.verify(public_inputs, &mutated_proof, "security_test");
        assert!(
            res.is_err(),
            "Verifier should have rejected proof with wrong request size instead of validating it!"
        );
    }
}

#[test]
fn test_reference_rle_infinite_loop_prevention() {
    use reference::ligero::read_ligero_proof;

    let config = LigeroConfig::default();

    // 1. Create dummy circuit
    let circuit_file = File::open(get_testdata_path("tests/testdata/sha256_circuit.lfc2"))
        .expect("Failed to open circuit file");
    let mut circuit_reader = BufReader::new(circuit_file);
    let circuit =
        parse_lfc2_bytes::<Gf2_128, _>(&mut circuit_reader).expect("Failed to parse LFC2 circuit");

    let sf = <Gf2_128 as Field>::Subfield::default();

    let witness_only_len = circuit.ninput - circuit.npublic_input;
    let mut pad_witness_len = 3 * circuit.layers.len();
    for layer in &circuit.layers {
        pad_witness_len += 4 * layer.logw;
    }
    let nw = witness_only_len + pad_witness_len;
    let nq = circuit.layers.len();
    let geom = reference::ligero::LigeroGeometry::new(&config, nw, nq);

    let mut stream = Vec::new();
    for _ in 0..geom.block_len {
        stream.extend_from_slice(&[0u8; 16]);
    }
    for _ in 0..geom.dblock_len {
        stream.extend_from_slice(&[0u8; 16]);
    }
    for _ in 0..geom.num_queries {
        stream.extend_from_slice(&[0u8; 16]);
    }
    for _ in 0..(geom.dblock_len - geom.block_len) {
        stream.extend_from_slice(&[0u8; 16]);
    }
    for _ in 0..geom.num_queries {
        stream.extend_from_slice(&[0u8; 32]);
    }

    // First runlen = 0
    stream.extend_from_slice(&0u32.to_le_bytes());
    // Second runlen = 0 (MUST BE REJECTED)
    stream.extend_from_slice(&0u32.to_le_bytes());

    let mut cursor = Cursor::new(stream);
    let result = read_ligero_proof::<Gf2_128, _>(&mut cursor, &config, &circuit, &sf);
    assert!(
        result.is_err(),
        "Repeated runlen = 0 must be rejected to prevent infinite loop!"
    );
}

#[test]
fn test_computed_circuit_id_matches_stored() {
    let sha256_file = File::open(get_testdata_path("tests/testdata/sha256_circuit.lfc2")).unwrap();
    let mut sha256_reader = BufReader::new(sha256_file);
    let sha256_circuit = parse_lfc2_bytes::<Gf2_128, _>(&mut sha256_reader).unwrap();
    let computed_sha256_id = compute_circuit_id(&sha256_circuit);
    assert_eq!(
        sha256_circuit.id, computed_sha256_id,
        "SHA256 computed circuit ID does not match stored circuit ID"
    );

    let ecdsa2_file = File::open(get_testdata_path("tests/testdata/ecdsa2_circuit.lfc2")).unwrap();
    let mut ecdsa2_reader = BufReader::new(ecdsa2_file);
    let ecdsa2_circuit = parse_lfc2_bytes::<P256, _>(&mut ecdsa2_reader).unwrap();
    let computed_ecdsa2_id = compute_circuit_id(&ecdsa2_circuit);
    assert_eq!(
        ecdsa2_circuit.id, computed_ecdsa2_id,
        "ECDSA2 computed circuit ID does not match stored circuit ID"
    );
}
