use std::{
    fs::File,
    io::{BufReader, Cursor},
    path::PathBuf,
};

use num_bigint::BigInt;
use reference::{
    algebra::{Field, Gf2_128, P256, Rng},
    circuit::{compute_circuit_id, parse_lfc2_bytes},
    ligero::LigeroConfig,
    zk::{ZkProver, ZkVerifier, read_zk_proof, write_zk_proof},
};

pub struct SimpleRng {
    pub state: u64,
}

impl SimpleRng {
    pub fn new(seed: u64) -> Self {
        Self { state: seed }
    }
}

impl Rng for SimpleRng {
    fn bytes(&mut self, len: usize) -> Vec<u8> {
        let mut out = Vec::with_capacity(len);
        let mut state = self.state;
        for _ in 0..len {
            state = state
                .wrapping_mul(6364136223846793005)
                .wrapping_add(1442695040888963407);
            out.push(((state >> 32) & 0xff) as u8);
        }
        self.state = state;
        out
    }
}

fn get_testdata_path(relative_path: &str) -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join(relative_path)
}

fn run_reference_test_helper<F: Field + 'static>(
    circuit_path: &str,
    inputs_path: &str,
    expected_proof_path: &str,
    config: LigeroConfig,
    seed: u64,
    label: &str,
    parse_input_fn: impl Fn(&str) -> F,
) {
    let circuit_full_path = get_testdata_path(circuit_path);
    let inputs_full_path = get_testdata_path(inputs_path);
    let expected_proof_full_path = get_testdata_path(expected_proof_path);

    // 1. Load Circuit
    let circuit_file = File::open(&circuit_full_path).expect("Failed to open circuit file");
    let mut circuit_reader = BufReader::new(circuit_file);
    let circuit_data =
        parse_lfc2_bytes::<F, _>(&mut circuit_reader).expect("Failed to parse LFC2 circuit");

    // 2. Load Inputs
    let inputs_file = File::open(&inputs_full_path).expect("Failed to open inputs file");
    let raw_inputs: Vec<String> =
        serde_json::from_reader(inputs_file).expect("Failed to parse inputs JSON");
    let inputs: Vec<F> = raw_inputs.iter().map(|s| parse_input_fn(s)).collect();

    // 3. Prover commit & prove
    let mut rng = SimpleRng::new(seed);
    let prover = ZkProver::new(circuit_data.clone(), config.clone());
    let commit = prover.commit(&inputs, &mut rng);
    let proof = prover.prove(&inputs, &commit, label);

    // 4. Compare generated bytes with expected bytes
    let sf = F::Subfield::default();
    let mut generated_bytes = Vec::new();
    write_zk_proof(&mut generated_bytes, &proof, &sf).unwrap();

    let expected_bytes =
        std::fs::read(&expected_proof_full_path).expect("Failed to read expected proof");
    assert_eq!(
        generated_bytes, expected_bytes,
        "Proof byte-for-byte mismatch!"
    );

    // 5. Verify the generated proof using ZkVerifier
    let verifier = ZkVerifier::new(circuit_data, config.clone());
    verifier
        .verify(&inputs[0..prover.circuit_data.npublic_input], &proof, label)
        .expect("ZkVerifier failed to verify the generated proof!");

    // 6. Verify the expected proof using ZkVerifier
    let mut expected_reader = Cursor::new(&expected_bytes);
    let expected_proof =
        read_zk_proof(&mut expected_reader, &prover.circuit_data, &sf, &config).unwrap();
    verifier
        .verify(
            &inputs[0..prover.circuit_data.npublic_input],
            &expected_proof,
            label,
        )
        .expect("ZkVerifier failed to verify the expected proof!");
}

#[test]
fn test_reference_sha256() {
    let parse_gf2_128 = |s: &str| {
        let val = BigInt::parse_bytes(s.as_bytes(), 10).unwrap();
        let mut v_bytes = val.to_bytes_le().1;
        v_bytes.resize(16, 0);
        Gf2_128::from_bytes(&v_bytes).unwrap()
    };
    let config = LigeroConfig {
        rate_inv: 3,
        num_queries: 16,
        encoded_len: 256,
    };
    run_reference_test_helper(
        "tests/testdata/sha256_circuit.lfc2",
        "tests/testdata/sha256_inputs.json",
        "tests/testdata/sha256_proof.bin",
        config,
        12345,
        "sha256_spec_test",
        parse_gf2_128,
    );
}

